    public void pintarLinha() {
        ArrayList<Integer> bugs = new ArrayList<>();

        for (int i = 0; i < lista.size(); i++) {
            if (lista.get(i).getTipo() == TipoToken.Erro) {
                bugs.add(i);
            }
        }

//        jtToken.setDefaultRenderer(Object.class, new ErrorLine(bugs));
    }

